    public int checkTrung(String tenDN) {
        int index = 0;
        String check = null;
        try {
            sql = "select MaNV,TenNV,GioiTinh, QueQuan,DiaChi,NgayLamViec,QuyenHan,matkhau from NhanVien where MaNV = ?";
            con = DBConnect.getConnection();
            ps = con.prepareStatement(sql);
            ps.setObject(1, tenDN);
            rs = ps.executeQuery(); //chạy câu lệnh select
            while (rs.next()) {
                index = 1;
                check = rs.getString(1);
            }
            if (index == 1 && tenDN.equalsIgnoreCase(check)) {
                return 1;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        return 0;
    }

