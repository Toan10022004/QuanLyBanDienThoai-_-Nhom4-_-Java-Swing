    private void btn_SignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SignUpActionPerformed
        // TODO add your handling code here:
        String maNV, tenNV, queQuan, diaChi, ngayLamViec, quyenHan, matkhau;
        int gioiTinh;
        maNV = txt_MaNV.getText();
        tenNV = txt_tenNV.getText();
        queQuan = txt_QueQuan.getText();
        diaChi = txt_DiaChi.getText();
        ngayLamViec = txt_NgayLamViec.getText();
        quyenHan = cbo_ChucVu.getSelectedItem().toString();
        matkhau = txt_MatKhau.getText();
        if (cbo_GioiTinh.getSelectedItem().toString().equalsIgnoreCase("Nam")) {
            gioiTinh = 1;
        } else {
            gioiTinh = 0;
        }

        try {
            if (maNV.isEmpty() || tenNV.isEmpty() || queQuan.isEmpty() || diaChi.isEmpty() || ngayLamViec.isEmpty() || quyenHan.isEmpty() || matkhau.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            } else if (checkTrung(maNV)== 1) {
                JOptionPane.showMessageDialog(this, "Tên đăng nhập đã tồn tại. Vui lòng nhập tên khác");
            } else {
                sql = "INSERT INTO NhanVien (MaNV, TenNV, GioiTinh, QueQuan, DiaChi, NgayLamViec, QuyenHan,matkhau)\n"
                        + "VALUES(?,?,?,?,?,?,?,?)";
                con = DBConnect.getConnection();
                ps = con.prepareStatement(sql);
                ps.setObject(1, maNV); //số 1 tương ứng với dấu hỏi chấm đầu tiên
                ps.setObject(2, tenNV);
                ps.setObject(3, gioiTinh);
                ps.setObject(4, queQuan);
                ps.setObject(5, diaChi);
                ps.setObject(6, ngayLamViec);
                ps.setObject(7, quyenHan);
                ps.setObject(8, matkhau);
                ps.executeUpdate();
                //update, dalete,insert: đểu dùng câu lệnh executeUpdate()
                JOptionPane.showMessageDialog(this, "Tạo tài khoản thành công");
            }
        } catch (Exception e) { // ko thêm đc
            e.printStackTrace();

        }
    }//GEN-LAST:event_btn_SignUpActionPerformed

