private Model_KhachHang readFormKhachHang() {
        String tenKH, sdt, email, diaChi;      
        tenKH = txt_HoTen.getText().trim();
        sdt = txt_SDT.getText().trim();
        email = txt_Email.getText().trim();
        diaChi = txt_DiaChi.getText().trim();
        if (tenKH.isEmpty()) { //isEmpty: nếu chưa nhập (rỗng)
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên");
            txt_HoTen.requestFocus();//đặt nháy chuột vào ô họ tên để yêu cầu nhâp
            return null; //dừng chương trình
        }if (sdt.isEmpty()) { //isEmpty: nếu chưa nhập (rỗng)
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập số điện thoại");
            txt_SDT.requestFocus();//đặt nháy chuột vào ô họ tên để yêu cầu nhâp
            return null; //dừng chương trình
        }
        if (email.isEmpty()) { //isEmpty: nếu chưa nhập (rỗng)
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập email");
            txt_Email.requestFocus();//đặt nháy chuột vào ô họ tên để yêu cầu nhâp
            return null; //dừng chương trình
        }
        if (diaChi.isEmpty()) { //isEmpty: nếu chưa nhập (rỗng)
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập địa chỉ");
            txt_DiaChi.requestFocus();//đặt nháy chuột vào ô họ tên để yêu cầu nhâp
            return null; //dừng chương trình
        }
        
        return new Model_KhachHang(tenKH, sdt, email, diaChi);
    }

