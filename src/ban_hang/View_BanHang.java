    private void tbl_DonHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_DonHangMouseClicked
        // TODO add your handling code here:       
        i = tbl_DonHang.getSelectedRow();
        int donGia = Integer.parseInt(tbl_DonHang.getValueAt(i, 4).toString()) / Integer.parseInt(tbl_DonHang.getValueAt(i, 3).toString());
        lbe_TenKH.setText(tbl_DonHang.getValueAt(i, 7).toString());
        lbe_TenSP.setText(tbl_DonHang.getValueAt(i, 8).toString());
        lbe_SL.setText(tbl_DonHang.getValueAt(i, 3).toString());
        lbe_Tong.setText(tbl_DonHang.getValueAt(i, 4).toString());
        lbe_DonGia.setText(String.valueOf(donGia));
        int tong = Integer.parseInt(tbl_DonHang.getValueAt(i, 4).toString());
        float thanhTien;
        if(tong > 30000000){
            lbe_KM.setText("30%");
            lbe_ThanhTien.setText(String.valueOf(tong*0.3));
        }else if(tong >25000000){
            lbe_KM.setText("25%");
            lbe_ThanhTien.setText(String.valueOf(tong*0.25));
        }else if(tong > 20000000){
            lbe_KM.setText("20%");
            lbe_ThanhTien.setText(String.valueOf(tong*0.2));
        }else if(tong > 15000000){
            lbe_KM.setText("15%");
            lbe_ThanhTien.setText(String.valueOf(tong*0.15));
        }
        else if(tong > 10000000){
            lbe_KM.setText("10%");
            lbe_ThanhTien.setText(String.valueOf(tong*0.1));
        }
    }//GEN-LAST:event_tbl_DonHangMouseClicked

